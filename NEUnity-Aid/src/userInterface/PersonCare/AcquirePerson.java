    public void populateWorkRequest() {
        DefaultTableModel table = (DefaultTableModel) workTable.getModel();
        table.setRowCount(0);
        for (TaskRequest req : personCareOrganization.getTaskQueue().getTaskRequestList()) {
            if (req instanceof PersonCareTaskRequest) {
                PersonCareTaskRequest request = (PersonCareTaskRequest) req;
                if (request.isIsAcquiredReq()) {
                    Object[] row = new Object[table.getColumnCount()];
                    row[0] = req;
                    row[1] = req.getSender();
                    row[2] = req.getReceiver();
                    row[3] = req.getPersonID();
                    row[4] = req.getPersonName();
                    row[5] = req.getStatus();
                    table.addRow(row);
                }
            }
        }
    }


